		<br>在我轉身打算要離開時，她忽然抓住我的手，要我趕快把禮物給她。
		<br>望著她堅定的眼神，我知道今天不送是回不了家了．．．。
		<br>我仰頭望天，雨煞那停止。
		<br>心道難道老天要我『今日事，今日畢』嗎．．？好吧，我只好順應天命。
