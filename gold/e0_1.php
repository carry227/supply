        <br>那天，一如往昔下班後我騎車送小凌回家，在回家的路途上，我告訴小凌我要送她一個禮物。
		<br>她懷疑的看看我，因為她看不到我身上有任何禮物，就問我：「到底要送什麼給她」。
		<br>
        我回答：「禮物在我身上，可以說有形，也可以說無形。不過若你想收到這份禮物，就必須在我送禮時閉上雙眼」。 <br>
